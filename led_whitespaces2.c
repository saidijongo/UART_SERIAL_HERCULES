void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
    if (huart->Instance == USART2) {
        static uint16_t rx_index = 0;

        if (rx_data[0] != '\n') {
            if (rx_index < RX_BUFFER_SIZE - 1) {
                rx_buffer[rx_index++] = rx_data[0];
            }
        } else {
            rx_buffer[rx_index] = '\0';  // Null-terminate the string
            HAL_UART_Transmit(&huart2, (uint8_t*)"Received: ", 10, 1000);
            HAL_UART_Transmit(&huart2, rx_buffer, rx_index, 1000);
            HAL_UART_Transmit(&huart2, (uint8_t*)"\n", 1, 1000);
            process_command((char*)rx_buffer);  // Process the received command
            rx_index = 0;  // Reset index for next reception
        }

        // Receive the next byte
        HAL_UART_Receive_IT(&huart2, rx_data, 1);
    }
}

void process_command(char *command) {
    // Remove any leading or trailing whitespace or quotes
    char *start = command;
    while (*start == ' ' || *start == '\"') start++;
    char *end = start + strlen(start) - 1;
    while (*end == ' ' || *end == '\"') end--;
    *(end + 1) = '\0';

    if (strcmp(start, "LED ON") == 0) {
        // Turn on the LED
        HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);
        HAL_UART_Transmit(&huart2, (uint8_t*)"LED ON\n", 7, 1000);
    } else if (strcmp(start, "LED OFF") == 0) {
        // Turn off the LED
        HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
        HAL_UART_Transmit(&huart2, (uint8_t*)"LED OFF\n", 8, 1000);
    } else {
        HAL_UART_Transmit(&huart2, (uint8_t*)"Unknown Command\n", 16, 1000);
    }
}

